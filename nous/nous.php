    <h1>Histoire de la marque</h1>

    <p>Sanitec est un collectif de passionnés de technologie qui mettent leurs talents
        à profit des autres dans le domaine de la santé. Alban de Braquilanges est le président et fondateur du groupe
        depuis 2018 et a vocation à changer la vie des gens. Le marché du textile en pleine expansion, 
        Ils se sont donné le défi de réaliser des vêtements connectés en commençant par les bébés. Ces derniers sont faibles et peuvent courir de 
        grands risques si on ne veille pas sur eux. C'est ainsi qu'Alban et ses coéquipiers ont eu l'ambition de créer Baby Serenity, un dispositif 
        électronique intégré au body du bébé pour évaluer ses paramètres sanitaires pour le plus grand bonheur des parents. 
    </p><br /><br />
    <p>
        Alban est parti du constat que de nombreuses morts subites du nourisson auraient pu être évitées si l'état de santé des bébés était surveillé en continu. Il a alors fondé l'entreprise et a ramené ses amis dans cette grande aventure ! Dès lors, la société ne cesse d'accroître en bourse et compte plus de 200 employés à travers la France. Alban est principalement entouré de son équipe de choc comprenant Alexis à la communication, Ryan à la stratégie marketing, Ivan au secrétariat et Dan à la trésorerie.
    </p>



